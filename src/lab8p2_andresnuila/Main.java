                tf_marca.setText("");
                tf_tama.setText("");
                tf_calidad.setText("");

                tf_tallaz.setText("");
                ta_descz.setText("");
                tf_comodidadz.setText("");

                JOptionPane.showMessageDialog(this, "Objeto agregado exitosamente.");
                DefaultTreeModel t = (DefaultTreeModel) jt_objetos.getModel();
                DefaultMutableTreeNode raiz = (DefaultMutableTreeNode) t.getRoot();
